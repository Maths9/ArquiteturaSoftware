public class Funcionario {

	Funcionario funcionario = new Funcionario();
	
	public void adicionarFuncionario(Funcionario f) {
		funcionario.adicionarFuncionario(f);
	}

	public Funcionario getFuncionario(Funcionario f) {
		return funcionario.getFuncionario(f);
	}
	
	public Funcionario getFuncionarioViaLoginESenha(Funcionario f) {
		return funcionario.getFuncionarioViaLoginESenha(f);	
	}
		
	public Funcionario getFuncionarioCodigo(Funcionario f) {
		return funcionario.getFuncionarioCodigo(f);	
	}

	public void removerFuncionario(Funcionario f) {
		funcionario.removerFuncionario(f);
	}

	public void editarFuncionario(Funcionario f) {
		funcionario.editarFuncionario(f);
	}

	public Funcionario getFuncionarios() {
		return funcionario.getFuncionarios();
	}
	
	
	

	
}